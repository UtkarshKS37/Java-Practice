class arithematicoperators {
    public static void main(String[] args) {
        int no1 = 6;
        int no2 = 7;
        //addition
        System.out.println(no1 + no2);
        //subtraction
        System.out.println(no1 - no2);
        //multiplication
        System.out.println(no1 * no2);
        //division
        System.out.println(no1 / no2); //since it's int/int so ans is also int and not decimal; double can be used instead of int to get decimal value & if one no is int and one is double then ans will also be double
        //remainder (modulus)
        System.out.println(no1 % no2);
    }
}
