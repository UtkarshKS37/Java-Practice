public class Main {
    public static void main(String[] args) {
        int mod = 21%2;
        System.out.println(mod);
        int x = 7;
        System.out.println(++x); //prefix operation
        System.out.println(x++); //postfix increment operation
        System.out.println(x);
        System.out.println(--x); //prefix decrement operation
        System.out.println(x);
    }
}