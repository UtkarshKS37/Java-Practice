class assignmentoperators {
    public static void main(String[] args) {
        int no = 6;
        no += 3; //is the same as no = no + 3;
        no -= 2; //is the same as no = no - 2;
        no *= 2; //is the same as no = no * 2;
        no /= 3; //is the same as no = no / 3;
        no %= 5; //is the same as no = no % 5;
        System.out.println(no); //ans will have calculated all above equations
    }
}
