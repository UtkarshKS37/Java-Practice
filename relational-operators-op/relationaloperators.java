class relationaloperators {
    public static void main(String[] args) {
        int no1 = 69;
        int no2 = 88;
        System.out.println(no1 == no2);
        System.out.println(no1 != no2);
        System.out.println(no1 > no2);
        System.out.println(no1 < no2);
        System.out.println(no1 >= no2);
        System.out.println(no1 <= no2);
    }
    
}
